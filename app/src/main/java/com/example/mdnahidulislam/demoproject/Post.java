package com.example.mdnahidulislam.demoproject;

public class Post {

    private String smile,anger,beauty,nature;

public Post(String smile,String anger,String beauty,String nature)
{
    this.smile=smile;
    this.anger=anger;
    this.beauty=beauty;
    this.nature=nature;



}
    public String getSmile() {

        return smile;
    }

    public String getAnger() {
        return anger;
    }

    public String getBeauty() {
        return beauty;
    }

    public String getNature() {
        return nature;
    }
}
